package org.jimsey.projects.turbine.condenser;

import org.apache.commons.lang3.builder.ReflectionToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.context.ApplicationListener;
import org.springframework.messaging.simp.stomp.StompHeaderAccessor;
import org.springframework.stereotype.Component;
import org.springframework.web.socket.messaging.SessionSubscribeEvent;

/**
 * Hooks into subscription events to/from stomp. Only works for direct connections, not when using a broker.
 *
 * @author the-james-burton
 */
@Component
public class StompSubscribeEvent implements ApplicationListener<SessionSubscribeEvent> {

  private static final Logger logger = LoggerFactory.getLogger(StompSubscribeEvent.class);

  @Override
  public void onApplicationEvent(SessionSubscribeEvent event) {
    StompHeaderAccessor sha = StompHeaderAccessor.wrap(event.getMessage());
    logger.info("Subscribe event: {}", ReflectionToStringBuilder.toString(sha, ToStringStyle.JSON_STYLE));
  }

}